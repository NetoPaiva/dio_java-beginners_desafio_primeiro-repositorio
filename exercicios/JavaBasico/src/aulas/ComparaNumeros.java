package aulas;

import java.util.Scanner;

public class ComparaNumeros {

    public static void main(String[] args) {

        int numero1 = 38;
        int numero2 = 90;

        if (numero1 == numero2)
            System.out.println("São iguais!");
        else if (numero1 != numero2)
            System.out.println("Não são iguais!");

    }
}